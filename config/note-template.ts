/**
 * 노트 요약 양식 (템플릿) 정의.
 *
 * 사용자는 `app/settings/page.tsx`에서 활성 템플릿을 편집할 수 있고, 결과는 Dropbox vault의
 * `.config/note-template.md`에 저장된다. 처리 시점에 `lib/dropbox/template.ts`가 이 파일을
 * 읽어서 `lib/ai/prompts.ts::buildSummaryPrompt`의 "출력 형식" 영역을 교체한다.
 *
 * Dropbox에서 읽기 실패 시(파일 없음/네트워크 오류 등) `DEFAULT_TEMPLATE`로 폴백한다.
 *
 * ## 지원 플레이스홀더
 * - `{{title}}` — 영상 제목 (H1 헤딩에 사용)
 * - `{{categoryTag}}` — 슬러그화된 카테고리명 (해시태그에 사용)
 *
 * 그 외 메타데이터(채널/카테고리/검색어/원본 자막 언어 등)는 프롬프트의 "영상 정보" 섹션에서
 * 별도로 들어가므로 템플릿 안에서는 다시 표기할 필요가 없다.
 */

export type NoteTemplateExample = {
  /** 파일 시스템과 코드에서 쓰는 식별자. Dropbox `.config/note-template-examples/<slug>.md` 경로에 매핑. */
  slug: string;
  /** 설정 페이지 UI에 표시되는 사람이 읽는 이름. */
  name: string;
  /** 설정 페이지 UI에 표시되는 한 줄 설명. */
  description: string;
  /** 실제 템플릿 마크다운 본문. */
  content: string;
};

